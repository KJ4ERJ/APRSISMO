function colors:getColorName(r, g, b)
	if type(r) == 'table' and #r == 3 then
		r, g, b = r[1], r[2], r[3]
	end
	--print('getColorName('..tostring(r)..' '..tostring(g)..' '..tostring(b)..')')
	for n,a in pairs(colors) do
		if type(a) == 'table' and #a == 3 then
			if a[1] == r and a[2] == g and a[3] == b then
				return n
			end
		--else print('colors['..tostring(n)..'] is type:'..type(a))
		end
	end
	return '*Unknown*'
end

function colors:getColorRGB(name)
	local color = colors[name]
	if color and #color == 3 then
		return color[1], color[2], color[3]
	end
	return nil
end

function colors:getColorArray(name)
	local color = colors[name]
	if color and #color == 3 then
		return color
	end
	return nil
end

function colors:getRandomTrackColorRGB()
	return colors:getColorRGB(colors.TrackColors[math.random(#colors.TrackColors)])
end

function colors:getRandomTrackColorArray()
	return colors:getColorArray(colors.TrackColors[math.random(#colors.TrackColors)])
end

